// Activity 3: Data Types 
    // Task 4: Create variables of different data types (number, string, boolean, object, array) and log each variable's type using the typeof operator.
        let var1  = 10;
        console.log(`Varialbe: ${var1}, Type is of type ${typeof var1}`); // Output: 10
        let var2  = "Hello World";
        console.log(`Variable: ${var2}, Type is of type ${typeof var2}`); // Output: Hello World
        let var3  = true;
        console.log(`Variable: ${var3}, Type is of type ${typeof var3}`); // Output: boolean
        let var4  = { name: "Kushaal Rana", age: 30 };
        console.log(`Variable: ${var4.name}, Type is of type ${typeof var4}`); // Output: object
        let var5  = [1, 2, 3, 4, 5];
        console.log(`Variable: ${var5}, Type is of type ${typeof var5}`); // Output: object

//Activity 4: Reassigning Variables
    // Task 5: Declare a varialbe using let, assign it an initial value, reassign a new value, and log both values to console.
        let initialValue = 10
        console.log(`Initial Value: ${initialValue}`); // Output: 10
        initialValue = 20
        console.log(`Reassigned Value: ${initialValue}`) //20

